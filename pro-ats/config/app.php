Step 1: Candidate Onboarding Candidates enter the system through CV inbox, manual entry, or contact managemet . Each candidate is assigned a unique code and a recruiter. The initial status is "new."

Step 2: Candidate Screening The assigned recruiter reviews the candidate profile, updates missing information, uploads documents, and changes the status to "qualified" if suitable or "rejected" if not a fit. Activity is logged automatically.

Step 3: Job & Client Setup Clients are onboarded with basic details. Jobs are created with detailed requirements and linked to clients. Jobs have a status workflow: draft → open → filled → closed.

Step 4: Candidate Submission (Unified): use only Submissions module with this simplified workflow:

